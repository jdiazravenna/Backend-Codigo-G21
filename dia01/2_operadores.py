num1 = 20
num2 = 40

# Operadores aritméticos

#Suma
suma = num1 + num2
print(suma)

#Resta
resta = num1 - num2
print(resta)

#Multiplicacion
multiplicacion = num1 * num2
print(multiplicacion)

#Division
division = num1 / num2
print(division)

#Division Entera
division_entera = num1 // num2
print(division_entera)

#modulo
modulo = num1 % num2
print(modulo)

#Potencia
potencia = num1 ** num2
print(potencia)

#Raiz
raiz = num1 ** (1/2) # Raiz Cuadrada
raiz2 = num1 ** (1/3) # Raiz Cúbica y así sucesivamente

division3 = (1/2)/(1/2)

#Comparacion

#es igual que
print(num1 == num2)
#Diferente que
print(num1 != num2)
#Mayor que
print(num1 > num2)
#Menor que
print(num1 < num2)
#mayor  o igual que
print(num1 >= num2)
#Menor o igual que
print(num1 <= num2)


## LOGICOS
# AND OR NOT

#AND
print(num1 > num2 and num1 != num2)

#OR

print(num1 > num2 or num1 != num2)

#NOT

